// переменные var
var name = 'John';
// старый вид объявления переменных
// объявления через var поднимаются в коде


// переменные let
let userName = 'Bill';
// не поднимается в коде наверх
// значение можно изменить


// постоянные, константа const
const secondName = 'David';
// не поднимается в коде наверх
// примитивы нельзя изменить
// объекты и массивы менять можно, но нельзя изменить на другой тип
// нельзя создать пустую переменную, выдаст ошибку
